/**
 * This code demonstrates the principle of inheritance.
 * Here we have a class that has 2 methods: start and stop. In the App.java file, where the main method is, we instantitate the class by creating an object from it, and then we 
 * call a couple of methods. 
 */
public class Machine {

	public void start() {
	 System.out.println("Machine started.");

	}
   public void stop() {
	   System.out.println("Machine stopped.");
   }
	
	
}

		
