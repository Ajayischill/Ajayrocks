/**
 * Here we have a sample of an object. An object is an instantiation of a class,
 * or an instance of a class. The class is a template. The object is a copy of
 * the template, which is then modified here with specific values for name and
 * age. This way the particulars can change while the class itself remains pure
 * and unchanged. In Java, most everything is an object. If you think of classes
 * as templates, and objects as copies of the template which are then modified,
 * it makes sense.
 * 
 */

public class Dog {

	String name;
	int age;

}
